export function uniqueRotations(type) {
  const seen = new Set();
  const rotations = [];
  SHAPES[type].forEach((cells, rotation) => {
    const key = cells
      .map(([x, y]) => `${x},${y}`)
      .sort()
      .join("|");
    if (!seen.has(key)) {
      seen.add(key);
      rotations.push(rotation);
    }
  });
  return rotations;
}
